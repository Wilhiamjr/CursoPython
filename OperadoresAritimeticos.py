number1 = 10;
number2 = 20;

#Soma
print(number1 + number2);

#Subtração
print(number1 - number2);

#Multiplicação
print(number1 * number2);

#Divisão
print(number1 / number2);

#Potenciação
print(number1 ** number2);

#Resto da divisão
print(number1 % number2);

#Divisão inteira
print(number1 // number2);
