saldo = 500;
print("Saldo inicial: ",saldo);
#adicionar mais valor ao existente
saldo +=100;
print('Saldo +100: ',saldo);
#subtrair valor do existente
saldo -=100;
print('Saldo -100: ',saldo);
#multiplicar valor do existente
saldo *=2;
print('Saldo *2: ',saldo);
#dividir valor do existente
saldo /=2;
print('Saldo /2: ',saldo);
#dividir inteiro
saldo //=2;
print('Saldo //2: ',saldo);
#modulo
saldo %=2;
print('Saldo %2: ',saldo);